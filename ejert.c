void ejercicio(unsigned char *trama, char j) {
	unsigned short tot = (trama[12]<<8) + trama[13];		
	unsigned char i;
	unsigned char s [][5] = {"RR", "RNR", "REJ", "SREJ"};
	//1.- Trama IP- ICMP con opciones imprimir opciones en hexadécimal 
	if(tot == 2048) {
		if(trama[23] == 1) {
			if((trama[14]&15) > 5) {
				printf("\n\n 	Ejericio 1\n");
				printf(" 	Trama %d\n", j);
				printf("	Trama con opciones: ");
				i = 34;
				for(i; i < ((trama[14]&15)*4)+14; i++) {
					printf("%02x ", trama[i]);	
				}
			}
		}
		//3.- La trama que sirve para establecer una comunicación orientada a conexión, imprimir MAC origen.
		else if(trama[23] == 6) {
			if(trama[14 + (trama[14]&15)*4 + 13] & 2) {
				printf("\n\n 	Ejercicio 3\n");
				printf(" 	Trama %d\n", j);
				printf(" 	La MAC origen es: %02x:%02x:%02x:%02x:%02x:%02x", trama[6], trama[7], trama[8], trama[9], trama[10],
						trama[11]);
			}
		}
		//4.- Una trama UDP cuyo encapsulado IP no tenía opciones -- devolver el valor del Offset en decimal.
		else if(trama[23] == 17) { 
			if(((trama[14]&15)*4) == 20) {						
				printf("\n\n 	Ejercicio 4\n");					
				printf(" 	Trama %d\n", j);
				printf("	Offset: %i bytes", (((trama[20]&31)<<8)|(trama[21]))*8);
			}
		}
	}
	//2.- Trama RNR-respuesta, imprimir N(r)   (considerar modo extendido)
	else if(tot < 1500) {
		if((trama[16] & 3) == 1) {
			if(s[(trama[16]>>2) & 3] == s[1]){				
				if((trama[15] == 1) && (trama[17] == 1)){
					printf("\n\n 	Ejercicio 2\n");
					printf(" 	Trama %d\n", j);
					printf(" 	N(r)=%d\n", trama[17] >> 1);
				}
			}
		}
		//5.- Una trama LLC del tipo I -- devolver el N(R) y N(S) en decimal (considerar modo extendido) 
		else if(((trama[16] & 3) == 0) || ((trama[16] & 3) == 2)) {
			printf("\n\n 	Ejercicio 5\n");
			printf(" 	Trama %d\n", j);
			printf("    	N(s)=%d, N(r)=%d, ", trama[16]>>1, trama[17]>>1);
		}
	} 
	//6.- una trama ARP ATM InvReply-- 
	//devolver el valor de la dirección de hardware destino en hexadecimal separada por puntos.
	else if(tot == 2054) {
		if((((trama[14]<<8) | trama[15]) & 16) || (((trama[14]<<8) | trama[15]) & 19)) {					
			if(((trama[20]<<8) | trama[21]) == 4) {
				printf("\n\n 	Ejercicio 6\n");
				printf(" 	Trama %d\n", j);
				printf("	Direccion MAC de destino %02x:%02x:%02x:%02x:%02x:%02x",
						trama[32], trama[33], trama[34], trama[35], trama[36], trama[37]);
			}
		}
	}
}

